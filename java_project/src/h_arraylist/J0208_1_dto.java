package h_arraylist;

public class J0208_1_dto {
 String grade, name, ban, number;
	int kor, eng, mat, count;
	public J0208_1_dto(String grade, String name, String ban, String number, int kor, int eng, int mat, int count) {
		super();
		this.grade = grade;
		this.name = name;
		this.ban = ban;
		this.number = number;
		this.kor = kor;
		this.eng = eng;
		this.mat = mat;
		this.count = count;
	}
	public String getGrade() {
		return grade;
	}
	public String getName() {
		return name;
	}
	public String getBan() {
		return ban;
	}
	public String getNumber() {
		return number;
	}
	public int getKor() {
		return kor;
	}
	public int getEng() {
		return eng;
	}
	public int getMat() {
		return mat;
	}
	public int getCount() {
		return count;
	}
	
	
	
}
